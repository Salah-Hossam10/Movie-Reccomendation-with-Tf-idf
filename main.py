@app.route('/predict', methods=['POST'])
def predict():
    movie_title = request.form.get('movie')

    # Check if the movie title exists in the DataFrame
    if movie_title in movies_df['title'].values:
        # Transform the input movie title using the vectorizer
        title_vector = vectorizer.transform([movie_title])  # Ensure this is a 2D array

        # Get distances and indices from the KNN model
        distances, indices = model.kneighbors(title_vector, n_neighbors=6)

        recommendations = [f'Recommendations for "{movie_title}":']
        for i in range(1, len(distances.flatten())):  # Start from 1 to skip the input movie
            recommended_title = movies_df.iloc[indices.flatten()[i]]["title"]
            recommendations.append(f'{i}: {recommended_title}, with distance of {distances.flatten()[i]:.4f}')

        return render_template('result.html', recommendations=recommendations)
    else:
        return "Movie not found in the database.", 400  # Return a 400 error for movie not found

